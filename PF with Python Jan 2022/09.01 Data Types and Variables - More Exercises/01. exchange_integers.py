# 1.Exchange Integers
# Read two integer numbers and, after that, exchange their values. Print the variable values before and after the exchange, as shown below:
#
# Input
# 5
# 10
#
# Output
# Before:
# a = 5
# b = 10
# After:
# a = 10
# b = 5

a = int(input())
b = int(input())

print("Before:")
print(f"a = {a}")
print(f"b = {b}")
print("After:")
print(f"a = {b}")
print(f"b = {a}")
