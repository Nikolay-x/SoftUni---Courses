# 4.Редица числа 2k + 1
# Напишете програма, която чете число n, въведено от потребителя, и отпечатва всички числа  ≤  n от редицата: 1, 3, 7, 15, 31, …. Всяко следващо число се изчислява като умножим предишното с 2 и добавим 1.
#
# вход
# 3
#
# изход
# 1
# 3

n = int(input())

seq = 1

while seq <= n:
    print(seq)
    seq = 2 * seq + 1
